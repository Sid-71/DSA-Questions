class Solution
{
    public:
    //Function to return max value that can be put in knapsack of capacity W.
    int knapSack(int W, int wt[], int val[], int n) 
    { 
       vvi dp(n + 1 , vi(W+1));
       for(int i=1; i<= n ; i++)
       {
           for(int j=1; j<=W; j++)
           {
               // incldue 
              int inc =0;
               if(wt[i-1] <= j)
               {
                   inc = val[i-1] + dp[i-1][j-wt[i-1]];
               }
               int exc = dp[i-1][j];
               dp[i][j] = max(inc,exc);
           }
       }
       return dp[n][W];
      
    }
};

